def add(num1, num2):
    print("the result of addition is: ", num1 + num2)

def subtract(num1, num2):
    print("the result of subtraction is: ", num1 - num2)

def multiply(num1, num2):
    print("the result of multiplication is: ", num1 * num2)

def divide(num1, num2):
    print("the result of the division is: ", num1 / num2)

def potentiation(base, exponent):
    print("the result of the powering is: ", base**exponent)

def rounding(num):
    print("the result of rounding is: ", round(num))